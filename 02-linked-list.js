/*  GAME PLAN

    create an empty arr
    create a current
    while loop till nul,
    push the current value into array
    move the head to the next node
    return arr
*/

let linkedListValues = (head) => {
    const values = [];
    let current = head;

    while(current !== null) {
        values.push(current.val)
        current = current.next;
    }

    return values
}

